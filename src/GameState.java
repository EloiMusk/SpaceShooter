/**
 * Enum for the different states of the game.
 *
 * @author EloiMusk
 * @version 1.0
 */
public enum GameState {
    /**
     * The player is in the menu. (Not implemented yet)
     */
    MENU,
    /**
     * The start screen of the game is shown.
     */
    GAME_START,
    /**
     * The game is running.
     */
    GAME_RUNNING,
    /**
     * The game is paused. (Not Implemented yet)
     */
    GAME_PAUSED,
    /**
     * The game is over.
     */
    GAME_OVER,
    /**
     * The game is won.
     */
    GAME_WON
}
